"""Shared analytical-quality rules injected into every agent's system prompt.

These rules attack three specific failure modes observed in production reports:
1. Agents fabricate precise-sounding numbers from vague assumptions
2. Sector-wide stats (e.g. ICT GDP growth) used as proxy for sub-sectors
3. Agents disagree wildly (e.g. TAM = 5M vs TAM = 30M) with no reconciliation
"""

QUALITY_RULES_AR = """
## قواعد الجودة التحليلية (إلزامية — تُطبَّق قبل أي تحليل)

### ١. سم كل رقم بمصدره — لا استثناء
كل رقم في تحليلك يجب أن يكون مسبوقاً بأحد هذه الوسوم:
- `[DATA]`: رقم مأخوذ حرفياً من البيانات المرفقة (data.gov.bh، البنك الدولي، Sijilat، إلخ). اذكر اسم الـ dataset والسنة.
- `[ESTIMATE]`: تقدير محسوب من بيانات أخرى. اذكر صراحةً المنطق ("X مأخوذ من Y بافتراض Z").
- `[ASSUMPTION]`: افتراض غير مدعوم ببيانات. استخدمه باعتدال شديد.
- `[UNKNOWN]`: لا تتوفر بيانات. استخدمه بدلاً من اختراع رقم.

مثال صحيح:
> [DATA] نمو الناتج المحلي للاتصالات في الربع الرابع 2025: +9.5% (data.gov.bh، dataset 2-quarterly-growth-cp-change)
> [ESTIMATE] حصة قطاع الإعلام والتسويق ضمن المعلومات والاتصالات: 5-10% — افتراض بناء على متوسط أسواق المنطقة
> [ASSUMPTION] متوسط قيمة عقد إعلامي سنوي: 50,000 د.ب — لا توجد بيانات حقيقية، استخدمها كبداية فقط
> [UNKNOWN] العدد الفعلي لعقود الوساطة الموجودة حالياً

### ٢. ممنوع: استخدام إحصاءات قطاعية شاملة كدليل على فرع منها
- إذا كان نمو "المعلومات والاتصالات" 9.5%، **لا** تستخدمه كدعم لقطاع "الإعلام والتسويق" تحديداً. هذا تضليل.
- اذكر الرقم القطاعي الشامل ثم **خفّف** بصراحة: "هذا الرقم يشمل التليكوم والـIT بالكامل، حصة الإعلام منه غير معروفة".
- لا تستخدم نمو GDP الكلي لدولة كدعم لقرار في قطاع فرعي.

### ٣. عند غياب البيانات: قل "غير معروف" — لا تخترع
- ممنوع: "حوالي 350 شركة" بدون مصدر
- مسموح: "[UNKNOWN] العدد الدقيق للشركات النشطة في هذا القطاع — البيانات المتاحة على Sijilat لا تفصل بهذا الدقة"
- ممنوع: "متوسط قيمة الصفقة 5,000 د.ب" بدون مصدر
- مسموح: "[ASSUMPTION] متوسط قيمة الصفقة 5,000 د.ب — تخمين، يجب التحقق منه ميدانياً قبل أي قرار استثماري"

### ٤. الالتزام بالفرضيات الموحَّدة (Baseline Assumptions)
إذا تم تزويدك في سياق السوق بقسم `## الفرضيات الموحَّدة لهذا التحليل`، **يجب** استخدام أرقامه كما هي. لا تخترع TAM أو SAM مختلفاً عما هو مذكور هناك. لا أرقام موازية.

### ٥. مستوى الثقة يعكس البيانات الفعلية
- أكثر من 50% من الأرقام `[ASSUMPTION]` أو `[UNKNOWN]` → الثقة 4/10 أو أقل
- أكثر من 30% من الأرقام كذلك → الثقة 5-6/10
- معظم الأرقام `[DATA]` أو `[ESTIMATE]` مُحكم → ثقة 7+/10
- لا تعطِ ثقة 8+ إلا إذا كانت البيانات الفعلية تغطي جميع أبعاد التحليل
"""


SYNTHESIZER_RESOLUTION_RULES_AR = """
## قواعد الحسم — لا متوسطات مرجَّحة بين رؤى متناقضة

### ١. عند تناقض جوهري بين وكيلين، اختر جانباً وبرّر
ممنوع: "وكيل السوق يقول لا، وكيل المنافسة يقول نعم → الدرجة 6/10 (متوسط)"
مسموح: "وكيل السوق يقول لا بسبب صغر السوق ([DATA] 1.5M نسمة)، وكيل المنافسة يقول نعم بناء على [ASSUMPTION] غير مدعوم. **أرجِّح وكيل السوق** لأن حجته مبنية على بيانات حقيقية → الدرجة 4/10".

### ٢. تحقَّق من اتساق الأرقام بين الوكلاء
إذا اختلف الوكلاء في رقم أساسي (مثل TAM)، حدّد:
- ما الرقم الذي اعتمدوه؟
- أيّ رقم له أساس بيانات أقوى؟
- ما الرقم الموحد الذي ستعتمده أنت؟
أرجع ذلك في حقل `numerical_inconsistencies` من JSON.

### ٣. التحذير من الاستشهادات المضللة
إذا لاحظت أن أحد الوكلاء استخدم إحصاءة قطاعية شاملة كدليل لفرع منها (مثل GDP المعلومات والاتصالات لتبرير قرار في الإعلام والتسويق)، اذكر هذا في `misleading_citations`.

### ٤. الدرجة النهائية لا تتجاوز ثقة الأرقام
- إذا كانت الثقة المتوسطة للوكلاء أقل من 5، الدرجة النهائية لا تتعدى 5
- إذا كانت أكثر من 50% من الأرقام `[ASSUMPTION]` أو `[UNKNOWN]`، الـ verdict لا يتعدى "تحتاج دراسة أعمق"
- لا تعطِ "فرصة واعدة" أو أعلى إلا إذا كانت البيانات تدعم ذلك بقوة

### ٥. الـ overall_score ليس متوسطاً، بل حكماً صريحاً
احسب overall_score بناء على:
- 60% أقوى وكيلين دليلاً (ليس أعلاهما درجة)
- 40% توافر البيانات الحقيقية في التحليل ككل
ثم اضبطه بحذف 1-2 نقطة لو كانت فجوات البيانات حرجة.
"""
